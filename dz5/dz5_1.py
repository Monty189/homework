days=int(input("Введите число от 1 до 7:\n1.Понедельник\n2.Вторник\n3.Среда\n4.Четверг\n5.Пятница\n6.Суббота\n7.Воскресенье\n"))

if days == 1:
    print("Понедельник")

elif days == 2:
    print("Вторник")

elif days == 3:
    print("Среда")

elif days == 4:
    print("Четверг")

elif days == 5:
    print("Пятница")

elif days == 6:
    print("Суббота")

elif days == 7:
    print("Воскресенье")

else:
    print("Такого дня не существует!")